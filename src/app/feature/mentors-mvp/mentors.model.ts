export class Mentors {
    id : number;
    firstName : string;
    age : number;
    gender: string;

    constructor(
        id : number,
        firstName : string,
        age : number,
        gender: string)
        {
            this.id = id;
            this.firstName = firstName;
            this.age = age;
            this.gender = gender;    
        }

}